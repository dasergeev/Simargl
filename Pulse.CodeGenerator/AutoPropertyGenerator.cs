using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using System.Linq;
using System.Text;

namespace Pulse.CodeGenerator;

/// <summary>
/// 
/// </summary>
[Generator]
[CLSCompliant(false)]
public sealed class AutoPropertyGenerator : IIncrementalGenerator
{
    /// <summary>
    /// 
    /// </summary>
    /// <param name="context"></param>
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        // Находим поля, к которым применён атрибут [AutoProperty]
        var fieldsWithAttribute = context.SyntaxProvider
            .CreateSyntaxProvider(
                predicate: static (node, _) => IsFieldWithAttribute(node),
                transform: static (ctx, _) => GetFieldInfo(ctx))
            .Where(static f => f is not null)!;

        // Группируем по классу, чтобы не генерировать по одному файлу на поле
        var grouped = fieldsWithAttribute.Collect();

        context.RegisterSourceOutput(grouped, static (spc, fields) =>
        {
            var byClass = fields.GroupBy(f => f!.ClassName);

            foreach (var group in byClass)
            {
                var src = GenerateClassCode(group!);
                spc.AddSource($"{group.Key}.AutoProperties.g.cs", src);
            }
        });
    }

    private static bool IsFieldWithAttribute(SyntaxNode node)
        => node is FieldDeclarationSyntax f && f.AttributeLists.Count > 0;

    private static FieldInfo? GetFieldInfo(GeneratorSyntaxContext ctx)
    {
        var fieldSyntax = (FieldDeclarationSyntax)ctx.Node;
        var model = ctx.SemanticModel;

        foreach (var variable in fieldSyntax.Declaration.Variables)
        {
            var symbol = model.GetDeclaredSymbol(variable) as IFieldSymbol;
            if (symbol == null)
                continue;

            bool hasAttr = symbol.GetAttributes().Any(a =>
                a.AttributeClass?.Name == "AutoPropertyAttribute" ||
                a.AttributeClass?.ToDisplayString() == "Pulse.AutoPropertyAttribute");

            if (!hasAttr)
                continue;

            var classSymbol = symbol.ContainingType;
            return new FieldInfo(
                className: classSymbol.Name,
                @namespace: classSymbol.ContainingNamespace.ToDisplayString(),
                fieldName: symbol.Name,
                fieldType: symbol.Type.ToDisplayString(),
                classSymbol.DeclaredAccessibility);
        }

        return null;
    }

    private static string GenerateClassCode(IEnumerable<FieldInfo> fields)
    {
        var first = fields.First();
        var sb = new StringBuilder();

        string accessibility = first.Accessibility switch
        {
            Accessibility.Public => "public",
            Accessibility.Internal => "internal",
            Accessibility.Protected => "protected",
            Accessibility.Private => "private",
            Accessibility.ProtectedOrInternal => "protected internal",
            Accessibility.ProtectedAndInternal => "private protected",
            _ => "internal"
        };

        sb.AppendLine($"// <auto-generated />");
        sb.AppendLine($"namespace {first.Namespace};");
        sb.AppendLine();
        sb.AppendLine($"{accessibility} partial class {first.ClassName}");
        sb.AppendLine("{");

        foreach (var f in fields)
        {
            var propName = GetPropertyName(f.FieldName);
            sb.AppendLine($"    public {f.FieldType} {propName}");
            sb.AppendLine("    {");
            sb.AppendLine($"        get => {f.FieldName};");
            sb.AppendLine($"        set => {f.FieldName} = value;");
            sb.AppendLine("    }");
            sb.AppendLine();
        }

        sb.AppendLine("}");
        return sb.ToString();
    }

    private static string GetPropertyName(string fieldName)
    {
        var name = fieldName.TrimStart('_');
        if (string.IsNullOrEmpty(name))
            return "Property";
        return char.ToUpper(name[0]) + name.Substring(1);
    }

    private sealed class FieldInfo
    {
        public FieldInfo(string className, string @namespace, string fieldName, string fieldType, Accessibility accessibility)
        {
            ClassName = className;
            Namespace = @namespace;
            FieldName = fieldName;
            FieldType = fieldType;
            Accessibility = accessibility;
        }

        public string ClassName { get; }
        public string Namespace { get; }
        public string FieldName { get; }
        public string FieldType { get; }
        public Accessibility Accessibility { get; }
    }
}
