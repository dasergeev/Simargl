        WriteLog("Применена новая конфигурация реального датчика, выполняется перезапуск."); // Записывает сообщение о перезапуске по новой конфигурации.
    } // Завершает метод перезапуска сервиса.

    private async Task AcceptLoopAsync(TcpListener listener, CancellationToken cancellationToken) // Объявляет цикл ожидания подключений.
    { // Начинает тело цикла ожидания подключений.
        try // Начинает обработку сетевых исключений цикла ожидания.
        { // Начинает блок ожидания подключений.
            while (!cancellationToken.IsCancellationRequested) // Продолжает прослушивание, пока сервис не остановлен.
            { // Начинает цикл ожидания очередного клиента.
                TcpClient acceptedClient = await listener.AcceptTcpClientAsync(cancellationToken).ConfigureAwait(false); // Асинхронно принимает очередного клиента.
                acceptedClient.NoDelay = true; // Отключает алгоритм Нейгла для принятого клиента.
                ReplaceActiveClient(acceptedClient); // Переключает обработку на нового клиента.
            } // Завершает цикл ожидания очередного клиента.
        } // Завершает блок ожидания подключений.
        catch (OperationCanceledException) // Перехватывает штатную отмену цикла ожидания.
        { // Начинает ветку штатной отмены.
        } // Завершает ветку штатной отмены.
        catch (ObjectDisposedException) // Перехватывает штатное закрытие прослушивателя при остановке.
        { // Начинает ветку закрытия прослушивателя.
        } // Завершает ветку закрытия прослушивателя.
        catch (SocketException exception) when (!cancellationToken.IsCancellationRequested) // Перехватывает сетевую ошибку прослушивания.
        { // Начинает ветку сетевой ошибки прослушивания.
            ConnectionStatus = $"Ошибка прослушивания порта {FixedListenPort}: {exception.Message}."; // Публикует состояние ошибки прослушивания.
            WriteLog($"Ошибка прослушивания порта {FixedListenPort}: {exception.Message}"); // Записывает сообщение об ошибке прослушивания.
        } // Завершает ветку сетевой ошибки прослушивания.
    } // Завершает тело цикла ожидания подключений.

    private void ReplaceActiveClient(TcpClient newClient) // Объявляет метод переключения на нового клиента.
    { // Начинает тело метода переключения клиента.
        CancellationTokenSource newClientSource = new(); // Создаёт токен отмены для нового клиента.
        TcpClient? previousClient; // Хранит ранее активного клиента.
        CancellationTokenSource? previousClientSource; // Хранит токен отмены ранее активного клиента.

        lock (this.stateLock) // Захватывает блокировку для атомарной замены клиента.
        { // Начинает блок замены клиента.
            previousClient = this.activeClient; // Сохраняет ссылку на предыдущего клиента.
            previousClientSource = this.clientCancellationSource; // Сохраняет токен отмены предыдущего клиента.
            this.activeClient = newClient; // Назначает нового клиента активным.
            this.clientCancellationSource = newClientSource; // Назначает токен отмены нового клиента.
        } // Завершает блок замены клиента.

        previousClientSource?.Cancel(); // Отменяет обработку предыдущего клиента.
        previousClient?.Dispose(); // Закрывает соединение предыдущего клиента.
        previousClientSource?.Dispose(); // Освобождает токен отмены предыдущего клиента.
        ActiveClientEndpoint = newClient.Client.RemoteEndPoint?.ToString() ?? "Адрес клиента недоступен."; // Публикует адрес нового активного клиента.
