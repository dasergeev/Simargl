        this.activeClientEndpoint = "Клиент не подключён."; // Инициализирует стартовый адрес клиента.
        this.bufferDurationSeconds = 15d; // Устанавливает длительность буфера по умолчанию.
        this.validateChecksum = true; // Включает проверку CRC32 по умолчанию.
        WriteLog($"Сервис реального датчика подготовлен для порта {FixedListenPort}."); // Writes the service initialization message.
    } // Завершает тело конструктора.

    /// <summary> // Документирует состояние прослушивания.
    /// Получает признак активного прослушивания TCP-порта. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public bool IsRunning // Объявляет свойство активности сервиса.
    { // Начинает тело свойства.
        get => this.isRunning; // Возвращает текущее состояние прослушивания.
        private set => SetProperty(ref this.isRunning, value); // Обновляет состояние прослушивания.
    } // Завершает тело свойства.

    /// <summary> // Документирует номер порта.
    /// Получает фиксированный прослушиваемый номер TCP-порта реального датчика. // Уточняет значение свойства.
