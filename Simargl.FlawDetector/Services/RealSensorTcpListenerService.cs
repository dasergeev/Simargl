            WriteLog($"Получен некорректный кадр: {exception.Message}"); // Записывает сообщение о некорректном кадре.
        } // Завершает ветку некорректного кадра.
        catch (Exception exception) when (!cancellationToken.IsCancellationRequested) // Перехватывает прочие ошибки обработки клиента.
        { // Начинает ветку общей ошибки обработки.
            ConnectionStatus = $"Ошибка обработки датчика: {exception.Message}"; // Публикует состояние общей ошибки обработки.
