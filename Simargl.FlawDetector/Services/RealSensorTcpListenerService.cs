    public void Start() // Объявляет метод запуска прослушивания.
    { // Начинает тело метода запуска.
        if (IsRunning) // Проверяет, запущен ли сервис в данный момент.
        { // Начинает ветку уже активного сервиса.
            return; // Пропускает повторный запуск сервиса.
        } // Завершает ветку уже активного сервиса.

        TcpListener newListener = new(IPAddress.Any, FixedListenPort); // Создаёт новый TCP-прослушиватель на фиксированном порту источника.
        newListener.Server.NoDelay = true; // Отключает алгоритм Нейгла для более оперативного приёма данных.
        newListener.Start(); // Запускает прослушивание порта.
        WriteLog($"Запущено прослушивание TCP-порта {FixedListenPort}."); // Записывает сообщение о начале прослушивания.
        CancellationTokenSource cancellationSource = new(); // Создаёт новый токен отмены прослушивания.

        lock (this.stateLock) // Захватывает блокировку состояния перед публикацией нового прослушивателя.
        { // Начинает блок изменения состояния.
            this.listener = newListener; // Сохраняет активный прослушиватель.
            this.listenerCancellationSource = cancellationSource; // Сохраняет токен отмены прослушивания.
            this.firstTimestampSeconds = null; // Сбрасывает опорное время первой выборки.
            this.samples.Clear(); // Очищает буфер старых отсчётов перед новым запуском.
        } // Завершает блок изменения состояния.

        FramesReceived = 0L; // Сбрасывает счётчик кадров перед новым запуском.
        SamplesReceived = 0L; // Сбрасывает счётчик отсчётов перед новым запуском.
        ActiveClientEndpoint = "Ожидание клиента."; // Публикует состояние ожидания нового клиента.
        ConnectionStatus = $"Порт {FixedListenPort} прослушивается."; // Публикует сообщение о начале прослушивания.
        IsRunning = true; // Переводит сервис в состояние активного прослушивания.
        this.acceptLoopTask = Task.Run(() => AcceptLoopAsync(newListener, cancellationSource.Token)); // Запускает фоновый цикл ожидания клиентов.
    } // Завершает метод запуска прослушивания.

    public void Stop() // Объявляет метод остановки сервиса.
    { // Начинает тело метода остановки.
        CancellationTokenSource? listenerSource; // Объявляет локальный токен отмены прослушивания.
        CancellationTokenSource? clientSource; // Объявляет локальный токен отмены клиента.
        TcpListener? activeListener; // Объявляет локальный активный прослушиватель.
        TcpClient? previousClient; // Объявляет локальный активный клиент.

        lock (this.stateLock) // Захватывает блокировку перед демонтажом состояния.
        { // Начинает блок демонтажа состояния.
            listenerSource = this.listenerCancellationSource; // Сохраняет ссылку на токен отмены прослушивания.
            clientSource = this.clientCancellationSource; // Сохраняет ссылку на токен отмены клиента.
            activeListener = this.listener; // Сохраняет ссылку на активный прослушиватель.
            previousClient = this.activeClient; // Сохраняет ссылку на активный клиент.
            this.listenerCancellationSource = null; // Удаляет сохранённый токен отмены прослушивания.
            this.clientCancellationSource = null; // Удаляет сохранённый токен отмены клиента.
            this.listener = null; // Удаляет ссылку на прослушиватель.
            this.activeClient = null; // Удаляет ссылку на активный клиент.
            this.firstTimestampSeconds = null; // Сбрасывает опорное время буфера.
            this.samples.Clear(); // Очищает буфер принятых отсчётов.
        } // Завершает блок демонтажа состояния.

        listenerSource?.Cancel(); // Отменяет цикл прослушивания порта.
        clientSource?.Cancel(); // Отменяет цикл обработки клиента.
        previousClient?.Dispose(); // Закрывает активное клиентское соединение.
        activeListener?.Stop(); // Останавливает TCP-прослушиватель.
        listenerSource?.Dispose(); // Освобождает токен отмены прослушивания.
        clientSource?.Dispose(); // Освобождает токен отмены клиента.
        IsRunning = false; // Переводит сервис в состояние остановки.
        ConnectionStatus = "Источник отключён."; // Публикует сообщение об остановке источника.
        ActiveClientEndpoint = "Клиент не подключён."; // Сбрасывает адрес клиента.
        WriteLog("Прослушивание реального датчика остановлено."); // Записывает сообщение об остановке прослушивания.
    } // Завершает метод остановки сервиса.

