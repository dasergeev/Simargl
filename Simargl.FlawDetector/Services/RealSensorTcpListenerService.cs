    } // Завершает метод проверки конфигурации.
    /// <summary> // Документирует чтение точного объёма данных.
    /// Считывает из потока строго заданное количество байтов или завершает работу ошибкой. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <param name="networkStream">Сетевой поток чтения.</param> // Документирует поток чтения.
    /// <param name="buffer">Буфер для заполнения байтами.</param> // Документирует буфер чтения.
    /// <param name="cancellationToken">Маркер отмены чтения.</param> // Документирует маркер отмены.
    /// <exception cref="EndOfStreamException">Возникает, если поток закрыт раньше получения полного кадра.</exception> // Документирует ошибку обрыва потока.
    private static async Task ReadExactlyAsync(NetworkStream networkStream, byte[] buffer, CancellationToken cancellationToken) // Объявляет метод точного чтения байтов.
    { // Начинает тело метода точного чтения.
        int totalBytesRead = 0; // Инициализирует счётчик уже прочитанных байтов.
        while (totalBytesRead < buffer.Length) // Продолжает чтение, пока буфер не заполнен полностью.
        { // Начинает цикл поэтапного чтения из потока.
            int bytesRead = await networkStream.ReadAsync(buffer.AsMemory(totalBytesRead, buffer.Length - totalBytesRead), cancellationToken).ConfigureAwait(false); // Асинхронно читает следующую порцию данных из потока.
            if (bytesRead == 0) // Проверяет преждевременное завершение потока.
            { // Начинает ветку обрыва потока.
                throw new EndOfStreamException("Поток данных датчика завершился до получения полного кадра."); // Сообщает об обрыве потока данных.
            } // Завершает ветку обрыва потока.
            totalBytesRead += bytesRead; // Накопляет количество фактически прочитанных байтов.
        } // Завершает цикл поэтапного чтения из потока.
    } // Завершает метод точного чтения.
