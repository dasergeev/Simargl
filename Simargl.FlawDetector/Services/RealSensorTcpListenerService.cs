    private static uint ComputeCrc32(byte[] payload) // ????????? ????? ?????????? CRC32.
    { // Начинает тело метода вычисления CRC32.
        uint crc = 0xFFFFFFFFU; // Инициализирует регистр CRC32 стартовым значением.
        foreach (byte currentByte in payload) // Перебирает все байты полезной нагрузки кадра.
        { // Начинает цикл обработки байтов.
            crc ^= currentByte; // Добавляет текущий байт в регистр CRC.
            for (int bitIndex = 0; bitIndex < 8; bitIndex++) // Перебирает все биты текущего байта.
            { // Начинает цикл побитовой обработки.
                bool leastSignificantBitIsSet = (crc & 1U) == 1U; // Проверяет младший бит регистра CRC.
                crc >>= 1; // Сдвигает регистр CRC на один бит вправо.
                if (leastSignificantBitIsSet) // Проверяет необходимость применения полинома CRC32.
                { // Начинает ветку применения полинома.
                    crc ^= 0xEDB88320U; // Применяет отражённый полином CRC32 IEEE 802.3.
                } // Завершает ветку применения полинома.
            } // Завершает цикл побитовой обработки.
        } // Завершает цикл обработки байтов.
        return ~crc; // Возвращает инвертированное итоговое значение CRC32.
    } // Завершает метод вычисления CRC32.
