    private int FindNearestSampleIndex(double timeSeconds) // Объявляет метод поиска индекса отсчёта.
    { // Начинает тело метода поиска индекса.
        if (this.samples.Count == 0) // Проверяет наличие отсчётов в буфере.
        { // Начинает ветку пустого буфера.
            return -1; // Возвращает признак отсутствия отсчётов.
        } // Завершает ветку пустого буфера.
        int low = 0; // Инициализирует левую границу бинарного поиска.
        int high = this.samples.Count - 1; // Инициализирует правую границу бинарного поиска.
        while (low <= high) // Продолжает бинарный поиск, пока диапазон не исчерпан.
        { // Начинает цикл бинарного поиска.
            int middle = low + ((high - low) / 2); // Вычисляет текущую середину диапазона поиска.
            double currentTime = this.samples[middle].TimeSeconds; // Получает время текущего отсчёта.
            if (Math.Abs(currentTime - timeSeconds) < 0.000001d) // Проверяет точное совпадение по времени.
            { // Начинает ветку точного совпадения.
                return middle; // Возвращает индекс точного совпадения.
            } // Завершает ветку точного совпадения.
            if (currentTime < timeSeconds) // Проверяет расположение текущего времени относительно искомого.
            { // Начинает ветку смещения вправо.
                low = middle + 1; // Сдвигает левую границу вправо.
            } // Завершает ветку смещения вправо.
            else // Обрабатывает отсчёты, расположенные правее искомого времени.
            { // Начинает ветку смещения влево.
                high = middle - 1; // Сдвигает правую границу влево.
            } // Завершает ветку смещения влево.
        } // Завершает цикл бинарного поиска.
        if (low >= this.samples.Count) // Проверяет выход искомого времени за конец буфера.
        { // Начинает ветку времени после буфера.
            return this.samples.Count - 1; // Возвращает последний доступный отсчёт.
        } // Завершает ветку времени после буфера.
        if (high < 0) // Проверяет выход искомого времени до начала буфера.
        { // Начинает ветку времени до буфера.
            return 0; // Возвращает первый доступный отсчёт.
        } // Завершает ветку времени до буфера.
        double lowDistance = Math.Abs(this.samples[low].TimeSeconds - timeSeconds); // Вычисляет расстояние до правого кандидата.
        double highDistance = Math.Abs(this.samples[high].TimeSeconds - timeSeconds); // Вычисляет расстояние до левого кандидата.
        return lowDistance < highDistance ? low : high; // Возвращает индекс ближайшего кандидата.
    } // Завершает метод поиска индекса отсчёта.
