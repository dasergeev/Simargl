    private void AppendFrameSamples(RealSensorFrame frame) // Объявляет метод записи кадра в буфер.
    { // Начинает тело метода записи кадра.
        if (frame.Samples.Count == 0) // Проверяет наличие отсчётов в кадре.
        { // Начинает ветку пустого кадра.
            return; // Пропускает добавление пустого кадра.
        } // Завершает ветку пустого кадра.
        double sampleInterval = 1d / FixedSensorSampleRate; // Вычисляет шаг по времени между отсчётами датчика.
        lock (this.stateLock) // Захватывает блокировку для записи в общий буфер.
        { // Начинает блок записи в буфер.
            this.firstTimestampSeconds ??= frame.TimestampSeconds; // Запоминает первую метку времени для нормализации временной шкалы.
            double normalizedFrameTime = frame.TimestampSeconds - this.firstTimestampSeconds.Value; // Нормализует метку времени кадра относительно начала записи.
            double firstSampleTime = normalizedFrameTime - ((frame.Samples.Count - 1) * sampleInterval); // Вычисляет время первого отсчёта кадра.
            for (int sampleIndex = 0; sampleIndex < frame.Samples.Count; sampleIndex++) // Перебирает все отсчёты разобранного кадра.
            { // Начинает цикл добавления отсчётов в буфер.
                RealSensorVector sample = frame.Samples[sampleIndex]; // Получает текущий отсчёт кадра.
                double sampleTime = firstSampleTime + (sampleIndex * sampleInterval); // Вычисляет нормализованное время текущего отсчёта.
                this.samples.Add(new RealSensorSample(sampleTime, sample.X, sample.Y, sample.Z)); // Добавляет отсчёт в общий буфер.
            } // Завершает цикл добавления отсчётов в буфер.
            double minAllowedTime = normalizedFrameTime - BufferDurationSeconds; // Вычисляет минимальное время, которое ещё должно оставаться в буфере.
            this.samples.RemoveAll(sample => sample.TimeSeconds < minAllowedTime); // Удаляет устаревшие отсчёты вне окна буфера.
        } // Завершает блок записи в буфер.
        FramesReceived += 1L; // ??????????? ??????? ???????? ??????.
        SamplesReceived += frame.Samples.Count; // ??????????? ??????? ???????? ????????.
        ConnectionStatus = "??????????? ?????? ?? ????????? ???????."; // ????????? ????????? ????????? ?????? ??????.
        if (FramesReceived == 1L || (FramesReceived % 100L) == 0L) // Checks whether a receive-progress log line should be emitted.
        { // Begins the receive-progress branch.
            WriteLog($"Приняты данные датчика: кадров {FramesReceived}, отсчётов {SamplesReceived}."); // Writes the receive-progress message.
        } // Ends the receive-progress branch.
