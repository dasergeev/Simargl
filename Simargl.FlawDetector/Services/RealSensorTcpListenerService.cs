    public void UpdateActivation(bool isEnabled, double bufferDurationSeconds, bool validateChecksum) // Объявляет метод применения состояния источника.
    { // Начинает тело метода применения состояния.
        if (!isEnabled) // Проверяет необходимость полной остановки источника.
        { // Начинает ветку выключения источника.
            Stop(); // Останавливает прослушивание и обработку данных без проверки промежуточных параметров.
            return; // Завершает метод после остановки.
        } // Завершает ветку выключения источника.

        ValidateConfiguration(bufferDurationSeconds); // Проверяет допустимость новых параметров только для активного источника.
        WriteLog($"Источник реального датчика активирован: порт {FixedListenPort}, буфер {bufferDurationSeconds:F1} c, CRC {(validateChecksum ? "включена" : "выключена")}."); // Записывает факт активации источника и применяемую конфигурацию.
        bool requiresRestart = Math.Abs(BufferDurationSeconds - bufferDurationSeconds) > 0.0001d || ValidateChecksum != validateChecksum; // Определяет необходимость перезапуска прослушивания.
        BufferDurationSeconds = bufferDurationSeconds; // Сохраняет новую длину буфера.
        ValidateChecksum = validateChecksum; // Сохраняет новый режим проверки CRC.

        if (!IsRunning) // Проверяет, запущен ли сервис в данный момент.
        { // Начинает ветку первого запуска.
            Start(); // Запускает прослушивание с текущими параметрами.
            return; // Завершает метод после запуска.
        } // Завершает ветку первого запуска.

        if (requiresRestart) // Проверяет необходимость перестроения прослушивания.
        { // Начинает ветку перезапуска.
            Restart(); // Перезапускает прослушивание с новой конфигурацией.
        } // Завершает ветку перезапуска.
    } // Завершает метод применения состояния.

