        } // Завершает ветку общей ошибки обработки.
    } // Завершает цикл обработки активного клиента.

    private async Task<RealSensorFrame> ReadFrameAsync(NetworkStream networkStream, CancellationToken cancellationToken) // Объявляет метод чтения кадра.
    { // Начинает тело метода чтения кадра.
        byte[] headerBuffer = new byte[FrameIdentifierBytes.Length]; // Создаёт буфер идентификатора кадра.
        await ReadExactlyAsync(networkStream, headerBuffer, cancellationToken).ConfigureAwait(false); // Считывает идентификатор кадра из потока.
        if (!headerBuffer.AsSpan().SequenceEqual(FrameIdentifierBytes)) // Проверяет совпадение идентификатора кадра.
        { // Начинает ветку ошибки идентификатора.
            throw new InvalidDataException("Идентификатор кадра не соответствует формату RAILTEST."); // Сообщает о неверном идентификаторе кадра.
        } // Завершает ветку ошибки идентификатора.

        byte[] descriptorBuffer = new byte[sizeof(uint)]; // Создаёт буфер дескриптора данных.
        await ReadExactlyAsync(networkStream, descriptorBuffer, cancellationToken).ConfigureAwait(false); // Считывает дескриптор данных кадра.
        uint descriptorValue = BinaryPrimitives.ReadUInt32LittleEndian(descriptorBuffer); // Извлекает 32-битное поле описания данных в little-endian формате.
        int channelCount = (int)((descriptorValue >> 24) & 0xFFU); // Извлекает количество асинхронных каналов из старшего байта дескриптора.
        int dataLengthBytes = (int)(descriptorValue & 0x00FFFFFFU); // Извлекает длину блока данных из младших трёх байтов дескриптора.
        if (channelCount > 32) // Проверяет реалистичность числа асинхронных каналов.
        { // Начинает ветку недопустимого числа асинхронных каналов.
            throw new InvalidDataException($"Количество асинхронных каналов {channelCount} выходит за допустимые пределы."); // Сообщает о некорректном числе асинхронных каналов.
        } // Завершает ветку недопустимого числа асинхронных каналов.
        if (dataLengthBytes <= 0 || (dataLengthBytes % sizeof(float)) != 0) // Проверяет кратность длины блока данных размеру float.
        { // Начинает ветку ошибки длины данных.
            throw new InvalidDataException("Длина блока данных кадра должна быть положительной и кратной 4 байтам."); // Сообщает о некорректной длине блока данных.
        } // Завершает ветку ошибки длины данных.

        byte[] timestampBuffer = new byte[sizeof(ulong)]; // Создаёт буфер метки времени кадра.
        await ReadExactlyAsync(networkStream, timestampBuffer, cancellationToken).ConfigureAwait(false); // Считывает метку времени кадра.
        ulong timestampMilliseconds = BinaryPrimitives.ReadUInt64LittleEndian(timestampBuffer); // Извлекает миллисекундную метку времени кадра.

        byte[] dataBuffer = new byte[dataLengthBytes]; // Создаёт буфер массива данных кадра.
        await ReadExactlyAsync(networkStream, dataBuffer, cancellationToken).ConfigureAwait(false); // Считывает массив данных кадра.

        byte[] crcBuffer = new byte[sizeof(uint)]; // Создаёт буфер контрольной суммы кадра.
        await ReadExactlyAsync(networkStream, crcBuffer, cancellationToken).ConfigureAwait(false); // Считывает контрольную сумму кадра.
        uint expectedCrc = BinaryPrimitives.ReadUInt32LittleEndian(crcBuffer); // Извлекает контрольную сумму из кадра.
        if (ValidateChecksum) // Проверяет необходимость валидации CRC.
        { // Начинает ветку проверки CRC.
            byte[] checksumPayload = new byte[headerBuffer.Length + descriptorBuffer.Length + timestampBuffer.Length + dataBuffer.Length]; // Создаёт буфер для расчёта CRC по кадру без последнего поля.
            Buffer.BlockCopy(headerBuffer, 0, checksumPayload, 0, headerBuffer.Length); // Копирует идентификатор в буфер CRC.
            Buffer.BlockCopy(descriptorBuffer, 0, checksumPayload, headerBuffer.Length, descriptorBuffer.Length); // Копирует дескриптор в буфер CRC.
            Buffer.BlockCopy(timestampBuffer, 0, checksumPayload, headerBuffer.Length + descriptorBuffer.Length, timestampBuffer.Length); // Копирует метку времени в буфер CRC.
            Buffer.BlockCopy(dataBuffer, 0, checksumPayload, headerBuffer.Length + descriptorBuffer.Length + timestampBuffer.Length, dataBuffer.Length); // Копирует данные в буфер CRC.
            uint actualCrc = ComputeCrc32(checksumPayload); // Вычисляет контрольную сумму принятого кадра.
            if (actualCrc != expectedCrc) // Проверяет совпадение вычисленной и переданной CRC.
            { // Начинает ветку ошибки CRC.
                throw new InvalidDataException("Контрольная сумма кадра не совпадает."); // Сообщает о некорректной контрольной сумме.
            } // Завершает ветку ошибки CRC.
        } // Завершает ветку проверки CRC.

        int totalFloatCount = dataLengthBytes / sizeof(float); // Вычисляет полное число float-значений в полезной нагрузке.
        if (totalFloatCount < channelCount) // Проверяет, что полезная нагрузка не меньше асинхронного префикса.
        { // Начинает ветку ошибки недостаточной длины.
            throw new InvalidDataException("Полезная нагрузка кадра меньше числа асинхронных каналов."); // Сообщает о некорректной длине полезной нагрузки.
        } // Завершает ветку ошибки недостаточной длины.

        int measurementFloatCount = totalFloatCount - channelCount; // Вычисляет число float-значений координатных измерений после асинхронных каналов.
        if ((measurementFloatCount % 3) != 0) // Проверяет кратность координатной части трём осям.
        { // Начинает ветку ошибки структуры координатной части.
            throw new InvalidDataException($"Координатная часть кадра должна содержать тройки X/Y/Z после {channelCount} асинхронных каналов."); // Сообщает о некорректной структуре координатной части.
        } // Завершает ветку ошибки структуры координатной части.

        int sampleCount = measurementFloatCount / 3; // Вычисляет количество трёхкоординатных отсчётов в кадре.
        int measurementStartOffset = channelCount * sizeof(float); // Вычисляет смещение координатной части после асинхронных каналов.
        if (FramesReceived == 0L) // Проверяет, разбирается ли первый кадр новой сессии.
        { // Начинает ветку диагностики первого кадра.
            WriteLog($"Получен первый TCP-кадр: async-каналов {channelCount}, длина данных {dataLengthBytes} байт, отсчётов XYZ {sampleCount}."); // Записывает диагностические параметры первого кадра.
        } // Завершает ветку диагностики первого кадра.

        List<RealSensorVector> values = new(sampleCount); // Создаёт буфер отсчётов кадра.
        for (int sampleIndex = 0; sampleIndex < sampleCount; sampleIndex++) // Перебирает все трёхкоординатные отсчёты кадра.
        { // Начинает цикл разбора отсчётов.
            int valueOffset = measurementStartOffset + (sampleIndex * 3 * sizeof(float)); // Вычисляет смещение первой координаты текущего отсчёта.
            float xValue = BitConverter.Int32BitsToSingle(BinaryPrimitives.ReadInt32LittleEndian(dataBuffer.AsSpan(valueOffset, sizeof(float)))); // Извлекает ускорение по оси X из массива данных.
            float yValue = BitConverter.Int32BitsToSingle(BinaryPrimitives.ReadInt32LittleEndian(dataBuffer.AsSpan(valueOffset + sizeof(float), sizeof(float)))); // Извлекает ускорение по оси Y из массива данных.
            float zValue = BitConverter.Int32BitsToSingle(BinaryPrimitives.ReadInt32LittleEndian(dataBuffer.AsSpan(valueOffset + (2 * sizeof(float)), sizeof(float)))); // Извлекает ускорение по оси Z из массива данных.
            values.Add(new RealSensorVector(xValue, yValue, zValue)); // Добавляет разобранный отсчёт в список кадра.
        } // Завершает цикл разбора отсчётов.

        return new RealSensorFrame(timestampMilliseconds / 1000d, values); // Возвращает разобранный кадр реального датчика.
    } // Завершает метод чтения кадра.

