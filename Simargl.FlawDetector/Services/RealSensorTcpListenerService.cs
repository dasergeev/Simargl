    private readonly Lock stateLock = new(); // Хранит объект синхронизации для сетевого состояния и буфера.
    private TcpListener? listener; // Хранит активный TCP-прослушиватель.
    private CancellationTokenSource? listenerCancellationSource; // Хранит маркер отмены прослушивания порта.
    private CancellationTokenSource? clientCancellationSource; // Хранит маркер отмены активного клиентского соединения.
    private Task? acceptLoopTask; // Хранит задачу цикла ожидания новых подключений.
    private Task? clientProcessingTask; // Хранит задачу обработки текущего клиента.
    private TcpClient? activeClient; // Хранит текущий активный TCP-клиент.
    private string connectionStatus; // Хранит текстовое состояние подключения датчика.
    private string activeClientEndpoint; // Хранит адрес текущего клиента.
    private const int FixedListenPort = 49001; // Хранит фиксированный номер прослушиваемого порта реального датчика.
    private const int FixedSensorSampleRate = 2000; // Хранит фиксированную частоту дискретизации реального датчика.
    private double bufferDurationSeconds; // Хранит длину буфера реальных данных.
    private bool validateChecksum; // Хранит признак проверки CRC32.
    private bool isRunning; // Хранит признак активного прослушивания.
    private long framesReceived; // Хранит количество успешно принятых кадров.
    private long samplesReceived; // Хранит количество успешно принятых отсчётов.
    private double? firstTimestampSeconds; // Хранит время первого принятого отсчёта.
    private readonly List<RealSensorSample> samples = []; // Хранит отсчёты реального датчика в буфере.

    /// <summary> // Documents the log event.
