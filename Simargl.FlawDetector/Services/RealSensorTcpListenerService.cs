    /// </summary> // Ends XML documentation.
    public event Action<string>? LogEntryAdded; // Publishes short service log messages.
    /// <summary> // Документирует конструктор.
    /// Инициализирует сервис прослушивания реального датчика начальными параметрами. // Уточняет назначение конструктора.
    /// </summary> // Завершает XML-документацию конструктора.
    public RealSensorTcpListenerService() // Объявляет конструктор сервиса.
    { // Начинает тело конструктора.
