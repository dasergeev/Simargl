    private async Task ProcessClientAsync(TcpClient client, CancellationToken cancellationToken) // Объявляет цикл обработки активного клиента.
    { // Начинает тело цикла обработки клиента.
        try // Начинает обработку ошибок чтения активного клиента.
        { // Начинает блок обработки клиента.
            using NetworkStream networkStream = client.GetStream(); // Получает сетевой поток активного клиента.
            while (!cancellationToken.IsCancellationRequested) // Продолжает чтение, пока клиент активен.
            { // Начинает цикл чтения кадров клиента.
                RealSensorFrame frame = await ReadFrameAsync(networkStream, cancellationToken).ConfigureAwait(false); // Считывает и разбирает очередной кадр.
                AppendFrameSamples(frame); // Добавляет отсчёты кадра в буфер.
            } // Завершает цикл чтения кадров клиента.
        } // Завершает блок обработки клиента.
        catch (OperationCanceledException) // Перехватывает штатную отмену обработки клиента.
        { // Начинает ветку штатной отмены клиента.
        } // Завершает ветку штатной отмены клиента.
        catch (IOException exception) when (!cancellationToken.IsCancellationRequested) // Перехватывает разрыв соединения или ошибку чтения.
        { // Начинает ветку ошибки ввода-вывода клиента.
            ConnectionStatus = $"Соединение с клиентом разорвано: {exception.Message}"; // Публикует состояние разрыва соединения.
            ActiveClientEndpoint = "Клиент не подключён."; // Сбрасывает адрес активного клиента.
            WriteLog($"Соединение с активным клиентом датчика разорвано: {exception.Message}"); // Записывает сообщение о разрыве соединения.
        } // Завершает ветку ошибки ввода-вывода клиента.
        catch (InvalidDataException exception) when (!cancellationToken.IsCancellationRequested) // Перехватывает ошибку формата кадра.
        { // Начинает ветку некорректного кадра.
