    public int ListenPort => FixedListenPort; // Возвращает фиксированный номер прослушиваемого порта.

    /// <summary> // Документирует частоту датчика.
    /// Получает фиксированную частоту дискретизации реального датчика. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public int SensorSampleRate => FixedSensorSampleRate; // Возвращает фиксированную частоту дискретизации датчика.

    /// <summary> // Документирует длину буфера.
    /// Получает длительность хранимого буфера реальных данных в секундах. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public double BufferDurationSeconds // Объявляет свойство длины буфера.
    { // Начинает тело свойства.
        get => this.bufferDurationSeconds; // Возвращает текущую длину буфера.
        private set => SetProperty(ref this.bufferDurationSeconds, value); // Обновляет текущую длину буфера.
    } // Завершает тело свойства.

    /// <summary> // Документирует проверку CRC.
    /// Получает признак проверки контрольной суммы входящих кадров. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public bool ValidateChecksum // Объявляет свойство проверки CRC.
    { // Начинает тело свойства.
        get => this.validateChecksum; // Возвращает текущий признак проверки CRC.
        private set => SetProperty(ref this.validateChecksum, value); // Обновляет текущий признак проверки CRC.
    } // Завершает тело свойства.

    /// <summary> // Документирует строку состояния.
    /// Получает текстовое описание состояния прослушивания и обработки данных. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public string ConnectionStatus // Объявляет свойство строкового статуса.
    { // Начинает тело свойства.
        get => this.connectionStatus; // Возвращает текущий текстовый статус.
        private set => SetProperty(ref this.connectionStatus, value); // Обновляет текущий текстовый статус.
    } // Завершает тело свойства.

    /// <summary> // Документирует конечную точку клиента.
    /// Получает адрес текущего активного TCP-клиента. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public string ActiveClientEndpoint // Объявляет свойство адреса клиента.
    { // Начинает тело свойства.
        get => this.activeClientEndpoint; // Возвращает адрес активного клиента.
        private set => SetProperty(ref this.activeClientEndpoint, value); // Обновляет адрес активного клиента.
    } // Завершает тело свойства.

    /// <summary> // Документирует счётчик кадров.
    /// Получает количество успешно принятых и разобранных кадров. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public long FramesReceived // Объявляет свойство количества кадров.
    { // Начинает тело свойства.
        get => this.framesReceived; // Возвращает текущее количество кадров.
        private set => SetProperty(ref this.framesReceived, value); // Обновляет текущее количество кадров.
    } // Завершает тело свойства.

    /// <summary> // Документирует счётчик отсчётов.
    /// Получает количество принятых трёхкоординатных отсчётов. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public long SamplesReceived // Объявляет свойство количества отсчётов.
    { // Начинает тело свойства.
        get => this.samplesReceived; // Возвращает текущее количество отсчётов.
        private set => SetProperty(ref this.samplesReceived, value); // Обновляет текущее количество отсчётов.
    } // Завершает тело свойства.
    /// <summary> // Документирует обновление состояния.
    /// Включает или выключает прослушивание и при необходимости перестраивает конфигурацию TCP-источника. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <param name="isEnabled">Признак активности реального источника.</param> // Документирует флаг активности источника.
    /// <param name="bufferDurationSeconds">Длина буфера принятых данных в секундах.</param> // Документирует длину буфера.
    /// <param name="validateChecksum">Признак проверки контрольной суммы.</param> // Документирует признак проверки CRC.
    /// <exception cref="ArgumentOutOfRangeException">Возникает, если переданы недопустимые параметры прослушивания.</exception> // Документирует ошибку диапазона параметров.
    public void UpdateActivation(bool isEnabled, double bufferDurationSeconds, bool validateChecksum) // Объявляет метод применения состояния источника.
    { // Начинает тело метода применения состояния.
        if (!isEnabled) // Проверяет необходимость полной остановки источника.
        { // Начинает ветку выключения источника.
            Stop(); // Останавливает прослушивание и обработку данных без проверки промежуточных параметров.
            return; // Завершает метод после остановки.
        } // Завершает ветку выключения источника.
        ValidateConfiguration(bufferDurationSeconds); // Проверяет допустимость новых параметров только для активного источника.
        bool requiresRestart = Math.Abs(BufferDurationSeconds - bufferDurationSeconds) > 0.0001d || ValidateChecksum != validateChecksum; // Определяет необходимость перезапуска прослушивания.
        BufferDurationSeconds = bufferDurationSeconds; // Сохраняет новую длину буфера.
        ValidateChecksum = validateChecksum; // Сохраняет новый режим проверки CRC.
        if (!IsRunning) // Проверяет, запущен ли сервис в данный момент.
        { // Начинает ветку первого запуска.
            Start(); // Запускает прослушивание с текущими параметрами.
            return; // Завершает метод после запуска.
        } // Завершает ветку первого запуска.
        if (requiresRestart) // Проверяет необходимость перестроения прослушивания.
        { // Начинает ветку перезапуска.
            Restart(); // Перезапускает прослушивание с новой конфигурацией.
        } // Завершает ветку перезапуска.
    } // Завершает метод применения состояния.
    /// <summary> // Документирует доступ к буферу.
    /// Пытается вернуть ближайший к заданному времени трёхкоординатный отсчёт из буфера реального датчика. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <param name="timeSeconds">Модельное время, для которого требуется отсчёт.</param> // Документирует момент времени поиска.
    /// <param name="sample">Найденный отсчёт датчика.</param> // Документирует возвращаемый отсчёт.
    /// <returns><see langword="true"/>, если подходящий отсчёт найден; иначе <see langword="false"/>.</returns> // Документирует результат поиска.
    public bool TryGetSample(double timeSeconds, out RealSensorVector sample) // Объявляет метод чтения отсчёта из буфера.
    { // Начинает тело метода чтения отсчёта.
        lock (this.stateLock) // Захватывает блокировку для безопасного чтения буфера.
        { // Начинает блокировку буфера.
            if (this.samples.Count == 0) // Проверяет наличие отсчётов в буфере.
            { // Начинает ветку пустого буфера.
                sample = default; // Возвращает пустой отсчёт при отсутствии данных.
                return false; // Сообщает об отсутствии подходящего отсчёта.
            } // Завершает ветку пустого буфера.
            int index = FindNearestSampleIndex(timeSeconds); // Ищет индекс ближайшего отсчёта к заданному времени.
            if (index < 0) // Проверяет корректность найденного индекса.
            { // Начинает ветку отсутствия индекса.
                sample = default; // Возвращает пустой отсчёт при ошибке поиска.
                return false; // Сообщает об отсутствии отсчёта.
            } // Завершает ветку отсутствия индекса.
            RealSensorSample foundSample = this.samples[index]; // Получает найденный отсчёт из буфера.
            sample = new RealSensorVector(foundSample.X, foundSample.Y, foundSample.Z); // Возвращает найденный вектор ускорений.
            return true; // Сообщает об успешном чтении отсчёта.
        } // Завершает блокировку буфера.
    } // Завершает метод чтения отсчёта.
    /// <summary> // Документирует запуск прослушивания.
    /// Запускает TCP-прослушивание реального датчика. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <exception cref="SocketException">Возникает, если не удалось открыть TCP-порт.</exception> // Документирует возможную сетевую ошибку.
    public void Start() // Объявляет метод запуска прослушивания.
    { // Начинает тело метода запуска.
        if (IsRunning) // Проверяет, запущен ли сервис в данный момент.
        { // Начинает ветку уже активного сервиса.
            return; // Пропускает повторный запуск сервиса.
        } // Завершает ветку уже активного сервиса.
        TcpListener newListener = new(IPAddress.Any, FixedListenPort); // Создаёт новый TCP-прослушиватель на фиксированном порту источника.
        newListener.Server.NoDelay = true; // Отключает алгоритм Нейгла для более оперативного приёма данных.
        newListener.Start(); // Запускает прослушивание порта.
        WriteLog($"Запущено прослушивание TCP-порта {FixedListenPort}."); // Writes the listening-start message.
        CancellationTokenSource cancellationSource = new(); // Создаёт новый маркер отмены прослушивания.
        lock (this.stateLock) // Захватывает блокировку состояния перед публикацией нового прослушивателя.
        { // Начинает блок изменения состояния.
            this.listener = newListener; // Сохраняет активный прослушиватель.
            this.listenerCancellationSource = cancellationSource; // Сохраняет маркер отмены прослушивания.
            this.firstTimestampSeconds = null; // Сбрасывает опорное время первой выборки.
            this.samples.Clear(); // Очищает буфер старых отсчётов перед новым запуском.
        } // Завершает блок изменения состояния.
        FramesReceived = 0L; // Сбрасывает счётчик кадров перед новым запуском.
        SamplesReceived = 0L; // Сбрасывает счётчик отсчётов перед новым запуском.
        ActiveClientEndpoint = "Ожидание клиента."; // Публикует состояние ожидания нового клиента.
        ConnectionStatus = $"Порт {FixedListenPort} прослушивается."; // Публикует сообщение о начале прослушивания.
        IsRunning = true; // Переводит сервис в состояние активного прослушивания.
        this.acceptLoopTask = Task.Run(() => AcceptLoopAsync(newListener, cancellationSource.Token)); // Запускает фоновый цикл ожидания клиентов.
    } // Завершает метод запуска прослушивания.
    /// <summary> // Документирует остановку прослушивания.
    /// Останавливает прослушивание порта, разрывает активное клиентское соединение и очищает состояние. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    public void Stop() // Объявляет метод остановки сервиса.
    { // Начинает тело метода остановки.
        CancellationTokenSource? listenerSource; // Объявляет локальный маркер отмены прослушивания.
        CancellationTokenSource? clientSource; // Объявляет локальный маркер отмены клиента.
        TcpListener? activeListener; // Объявляет локальный активный прослушиватель.
        TcpClient? previousClient; // Объявляет локальный активный клиент.
        lock (this.stateLock) // Захватывает блокировку перед демонтажом состояния.
        { // Начинает блок демонтажа состояния.
            listenerSource = this.listenerCancellationSource; // Сохраняет ссылку на маркер отмены прослушивания.
            clientSource = this.clientCancellationSource; // Сохраняет ссылку на маркер отмены клиента.
            activeListener = this.listener; // Сохраняет ссылку на активный прослушиватель.
            previousClient = this.activeClient; // Сохраняет ссылку на активный клиент.
            this.listenerCancellationSource = null; // Удаляет сохранённый маркер отмены прослушивания.
            this.clientCancellationSource = null; // Удаляет сохранённый маркер отмены клиента.
            this.listener = null; // Удаляет ссылку на прослушиватель.
            this.activeClient = null; // Удаляет ссылку на активный клиент.
            this.firstTimestampSeconds = null; // Сбрасывает опорное время буфера.
            this.samples.Clear(); // Очищает буфер принятых отсчётов.
        } // Завершает блок демонтажа состояния.
        listenerSource?.Cancel(); // Отменяет цикл прослушивания порта.
        clientSource?.Cancel(); // Отменяет цикл обработки клиента.
        previousClient?.Dispose(); // Закрывает активное клиентское соединение.
        activeListener?.Stop(); // Останавливает TCP-прослушиватель.
        listenerSource?.Dispose(); // Освобождает маркер отмены прослушивания.
        clientSource?.Dispose(); // Освобождает маркер отмены клиента.
        IsRunning = false; // Переводит сервис в состояние остановки.
        ConnectionStatus = "Источник отключён."; // Публикует сообщение об остановке источника.
        ActiveClientEndpoint = "Клиент не подключён."; // Сбрасывает адрес клиента.
        WriteLog("Прослушивание реального датчика остановлено."); // Writes the stop-listening message.
    } // Завершает метод остановки сервиса.
    /// <summary> // Документирует освобождение ресурсов.
    /// Освобождает сетевые ресурсы сервиса реального датчика. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    public void Dispose() // Объявляет метод освобождения ресурсов.
    { // Начинает тело метода освобождения ресурсов.
        Stop(); // Останавливает сервис и закрывает сетевые ресурсы.
        GC.SuppressFinalize(this); // Подавляет финализацию после явного освобождения.
    } // Завершает метод освобождения ресурсов.
    /// <summary> // Документирует перезапуск.
    /// Полностью перезапускает прослушивание с текущей конфигурацией. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    private void Restart() // Объявляет метод перезапуска сервиса.
    { // Начинает тело метода перезапуска.
        Stop(); // Полностью останавливает текущую сессию прослушивания.
        Start(); // Повторно запускает сервис с текущими настройками.
