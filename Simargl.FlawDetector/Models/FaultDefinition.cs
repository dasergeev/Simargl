namespace Simargl.FlawDetector.Models; // Определяет пространство имён для описания дефектов.

/// <summary> // Описывает назначение record-типа.
/// Представляет настройку одного дефекта, участвующего в генерации сигнала. // Уточняет роль типа в симуляции.
/// </summary> // Завершает XML-документацию типа.
/// <param name="FaultType">Тип дефекта.</param> // Документирует тип дефекта.
/// <param name="DisplayName">Отображаемое имя дефекта.</param> // Документирует человекочитаемое имя.
/// <param name="IsEnabled">Признак участия дефекта в симуляции.</param> // Документирует флаг включения.
/// <param name="Severity">Нормированная тяжесть дефекта в диапазоне от 0 до 1.</param> // Документирует тяжесть дефекта.
/// <param name="ImpactFactor">Коэффициент ударного воздействия дефекта.</param> // Документирует базовую интенсивность воздействия.
internal sealed record FaultDefinition( // Объявляет неизменяемый тип настройки дефекта.
    BearingFaultType FaultType, // Хранит тип дефекта.
    string DisplayName, // Хранит отображаемое имя.
    bool IsEnabled, // Хранит состояние включения дефекта.
    double Severity, // Хранит величину тяжести дефекта.
    double ImpactFactor); // Хранит базовую интенсивность воздействия.

