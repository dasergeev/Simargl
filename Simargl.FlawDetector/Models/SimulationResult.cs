namespace Simargl.FlawDetector.Models; // Определяет пространство имён результата симуляции.

/// <summary> // Описывает назначение record-типа.
/// Содержит результаты расчёта вибросигнала и диагностические характеристики. // Уточняет состав результата симуляции.
/// </summary> // Завершает XML-документацию типа.
/// <param name="XAxis">Сигнал по продольной оси X.</param> // Документирует первую ось.
/// <param name="YAxis">Сигнал по поперечной оси Y.</param> // Документирует вторую ось.
/// <param name="ZAxis">Сигнал по вертикальной оси Z.</param> // Документирует третью ось.
/// <param name="WheelRotationFrequency">Частота вращения колеса в герцах.</param> // Документирует частоту колеса.
/// <param name="BPFI">Характеристическая частота дефекта внутреннего кольца.</param> // Документирует частоту BPFI.
/// <param name="BPFO">Характеристическая частота дефекта наружного кольца.</param> // Документирует частоту BPFO.
/// <param name="BSF">Характеристическая частота дефекта тела качения.</param> // Документирует частоту BSF.
/// <param name="FTF">Характеристическая частота дефекта сепаратора.</param> // Документирует частоту FTF.
/// <param name="Summary">Текстовая сводка по симуляции.</param> // Документирует итоговое описание.
internal sealed record SimulationResult( // Объявляет тип результата симуляции.
    AxisSignalSeries XAxis, // Хранит временной ряд оси X.
    AxisSignalSeries YAxis, // Хранит временной ряд оси Y.
    AxisSignalSeries ZAxis, // Хранит временной ряд оси Z.
    double WheelRotationFrequency, // Хранит частоту вращения колеса.
    double BPFI, // Хранит частоту дефекта внутреннего кольца.
    double BPFO, // Хранит частоту дефекта наружного кольца.
    double BSF, // Хранит частоту дефекта тела качения.
    double FTF, // Хранит частоту дефекта сепаратора.
    string Summary); // Хранит диагностическую сводку.

