namespace Simargl.FlawDetector.Models; // Определяет пространство имён моделей дефектов подшипника.

/// <summary> // Описывает назначение перечисления.
/// Перечисляет поддерживаемые типы дефектов буксового подшипника. // Уточняет предметную область перечисления.
/// </summary> // Завершает XML-документацию перечисления.
internal enum BearingFaultType // Объявляет типы дефектов подшипника.
{ // Начинает тело перечисления.
    /// <summary> // Документирует элемент перечисления.
    /// Дефект наружного кольца. // Уточняет физический смысл элемента.
    /// </summary> // Завершает XML-документацию элемента перечисления.
    OuterRace, // Задаёт дефект наружного кольца.

    /// <summary> // Документирует элемент перечисления.
    /// Дефект внутреннего кольца. // Уточняет физический смысл элемента.
    /// </summary> // Завершает XML-документацию элемента перечисления.
    InnerRace, // Задаёт дефект внутреннего кольца.

    /// <summary> // Документирует элемент перечисления.
    /// Дефект тела качения. // Уточняет физический смысл элемента.
    /// </summary> // Завершает XML-документацию элемента перечисления.
    RollingElement, // Задаёт дефект тела качения.

    /// <summary> // Документирует элемент перечисления.
    /// Дефект сепаратора. // Уточняет физический смысл элемента.
    /// </summary> // Завершает XML-документацию элемента перечисления.
    Cage, // Задаёт дефект сепаратора.

    /// <summary> // Документирует элемент перечисления.
    /// Дисбаланс колесной пары или подшипникового узла. // Уточняет физический смысл элемента.
    /// </summary> // Завершает XML-документацию элемента перечисления.
    Unbalance, // Задаёт дисбаланс вращающегося узла.

    /// <summary> // Документирует элемент перечисления.
    /// Перекос или несоосность узла. // Уточняет физический смысл элемента.
    /// </summary> // Завершает XML-документацию элемента перечисления.
    Misalignment, // Задаёт несоосность узла.
} // Завершает тело перечисления.

