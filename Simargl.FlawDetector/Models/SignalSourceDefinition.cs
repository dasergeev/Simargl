namespace Simargl.FlawDetector.Models; // Определяет пространство имён описаний источников сигнала.

/// <summary> // Описывает назначение record-типа.
/// Представляет настройку одного источника сигнала, участвующего в генерации вибрации. // Уточняет роль типа в симуляции.
/// </summary> // Завершает XML-документацию типа.
/// <param name="SourceType">Тип источника сигнала.</param> // Документирует тип источника сигнала.
/// <param name="DisplayName">Отображаемое имя источника.</param> // Документирует человекочитаемое имя источника.
/// <param name="IsEnabled">Признак участия источника в генерации сигнала.</param> // Документирует флаг включения источника.
/// <param name="AmplitudeScale">Масштаб амплитуды источника.</param> // Документирует амплитудный масштаб источника.
/// <param name="FrequencyScale">Масштаб частоты источника.</param> // Документирует частотный масштаб источника.
internal sealed record SignalSourceDefinition( // Объявляет неизменяемый тип настройки источника сигнала.
    SignalSourceType SourceType, // Хранит тип источника сигнала.
    string DisplayName, // Хранит отображаемое имя источника.
    bool IsEnabled, // Хранит состояние включения источника.
    double AmplitudeScale, // Хранит амплитудный масштаб источника.
    double FrequencyScale); // Хранит частотный масштаб источника.
