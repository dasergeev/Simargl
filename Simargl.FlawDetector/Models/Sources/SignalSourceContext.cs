namespace Simargl.FlawDetector.Models.Sources; // Определяет пространство имён контекста генерации источников.

/// <summary> // Описывает назначение record-типа.
/// Содержит параметры текущего отсчёта, общие для всех источников сигнала. // Уточняет область применения контекста.
/// </summary> // Завершает XML-документацию типа.
/// <param name="TimeSeconds">Абсолютное модельное время.</param> // Документирует модельное время.
/// <param name="RotationPhase">Фаза вращения колёсной пары.</param> // Документирует фазу вращения.
/// <param name="WheelRotationFrequency">Частота вращения колеса.</param> // Документирует частоту вращения.
/// <param name="Bpfi">Характеристическая частота внутреннего кольца.</param> // Документирует частоту BPFI.
/// <param name="Bpfo">Характеристическая частота наружного кольца.</param> // Документирует частоту BPFO.
/// <param name="Bsf">Характеристическая частота тела качения.</param> // Документирует частоту BSF.
/// <param name="Ftf">Характеристическая частота сепаратора.</param> // Документирует частоту FTF.
/// <param name="LoadScale">Коэффициент роста вибрации от нагрузки.</param> // Документирует коэффициент нагрузки.
internal sealed record SignalSourceContext( // Объявляет контекст расчёта источника.
    double TimeSeconds, // Хранит абсолютное модельное время.
    double RotationPhase, // Хранит фазу вращения колёсной пары.
    double WheelRotationFrequency, // Хранит частоту вращения колеса.
    double Bpfi, // Хранит частоту внутреннего кольца.
    double Bpfo, // Хранит частоту наружного кольца.
    double Bsf, // Хранит частоту тела качения.
    double Ftf, // Хранит частоту сепаратора.
    double LoadScale); // Хранит коэффициент роста вибрации от нагрузки.
