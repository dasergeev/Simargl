namespace Simargl.FlawDetector.Models.Sources; // Определяет пространство имён источников сигнала.

/// <summary> // Описывает назначение интерфейса.
/// Определяет общий абстрактный контракт любого источника сигнала акселерометра. // Уточняет роль интерфейса в генераторе.
/// </summary> // Завершает XML-документацию интерфейса.
internal interface ISignalSource // Объявляет общий интерфейс источника сигнала.
{ // Начинает тело интерфейса.
    /// <summary> // Документирует тип источника.
    /// Получает тип источника сигнала. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    SignalSourceType SourceType { get; } // Возвращает тип источника сигнала.

    /// <summary> // Документирует отображаемое имя.
    /// Получает имя источника для пользовательского интерфейса. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    string DisplayName { get; } // Возвращает отображаемое имя источника.

    /// <summary> // Документирует признак включения.
    /// Получает признак участия источника в формировании сигнала. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    bool IsEnabled { get; } // Возвращает признак включения источника.

    /// <summary> // Документирует накопление вклада.
    /// Добавляет вклад источника в текущий трёхкоординатный отсчёт сигнала. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <param name="context">Контекст расчёта текущего отсчёта.</param> // Документирует расчётный контекст.
    /// <param name="faults">Коллекция дефектов подшипника.</param> // Документирует набор дефектов.
    /// <param name="xSignal">Накопленный сигнал по оси X.</param> // Документирует продольную составляющую.
    /// <param name="ySignal">Накопленный сигнал по оси Y.</param> // Документирует поперечную составляющую.
    /// <param name="zSignal">Накопленный сигнал по оси Z.</param> // Документирует вертикальную составляющую.
    void Accumulate(SignalSourceContext context, IReadOnlyList<FaultDefinition> faults, ref double xSignal, ref double ySignal, ref double zSignal); // Добавляет вклад источника в текущий отсчёт.
}
