using System.Collections; // Подключает базовые интерфейсы перечисления для привязки коллекций.
using System.Windows; // Подключает базовые типы WPF и dependency properties.

namespace Simargl.FlawDetector.Controls; // Определяет пространство имён пользовательских контролов.

/// <summary> // Описывает назначение класса.
/// Представляет отдельный пользовательский контрол редактирования выбранного источника сигнала. // Уточняет роль контрола в интерфейсе.
/// </summary> // Завершает XML-документацию класса.
public partial class SignalSourceEditorControl : System.Windows.Controls.UserControl // Объявляет пользовательский контрол редактора источника.
{ // Начинает тело класса.
    /// <summary> // Документирует свойство зависимости выбранного источника.
    /// Идентифицирует свойство зависимости, содержащее выбранный источник сигнала. // Уточняет назначение поля.
    /// </summary> // Завершает XML-документацию поля.
    public static readonly DependencyProperty SourceProperty = DependencyProperty.Register( // Регистрирует свойство зависимости выбранного источника.
        nameof(Source), // Передаёт имя свойства зависимости.
        typeof(object), // Передаёт тип значения свойства.
        typeof(SignalSourceEditorControl), // Передаёт тип владельца свойства.
        new PropertyMetadata(null)); // Устанавливает метаданные свойства по умолчанию.

    /// <summary> // Документирует свойство зависимости списка дефектов.
    /// Идентифицирует свойство зависимости, содержащее список дефектов для источника дефектных импульсов. // Уточняет назначение поля.
    /// </summary> // Завершает XML-документацию поля.
    public static readonly DependencyProperty FaultsProperty = DependencyProperty.Register( // Регистрирует свойство зависимости списка дефектов.
        nameof(Faults), // Передаёт имя свойства зависимости.
        typeof(IEnumerable), // Передаёт тип значения свойства.
        typeof(SignalSourceEditorControl), // Передаёт тип владельца свойства.
        new PropertyMetadata(null)); // Устанавливает метаданные свойства по умолчанию.

    /// <summary> // Документирует конструктор контрола.
    /// Инициализирует новый экземпляр редактора источника сигнала. // Уточняет назначение конструктора.
    /// </summary> // Завершает XML-документацию конструктора.
    public SignalSourceEditorControl() // Объявляет конструктор контрола.
    { // Начинает тело конструктора.
        InitializeComponent(); // Инициализирует визуальное дерево пользовательского контрола.
    } // Завершает тело конструктора.

    /// <summary> // Документирует выбранный источник.
    /// Получает или задаёт источник сигнала, редактируемый текущим контролом. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public object? Source // Объявляет свойство выбранного источника.
    { // Начинает тело свойства выбранного источника.
        get => GetValue(SourceProperty); // Возвращает текущее значение свойства зависимости.
        set => SetValue(SourceProperty, value); // Обновляет текущее значение свойства зависимости.
    } // Завершает тело свойства выбранного источника.

    /// <summary> // Документирует список дефектов.
    /// Получает или задаёт коллекцию дефектов, доступную для редактирования в шаблоне дефектных импульсов. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public IEnumerable? Faults // Объявляет свойство списка дефектов.
    { // Начинает тело свойства списка дефектов.
        get => (IEnumerable?)GetValue(FaultsProperty); // Возвращает текущее значение свойства зависимости.
        set => SetValue(FaultsProperty, value); // Обновляет текущее значение свойства зависимости.
    } // Завершает тело свойства списка дефектов.
} // Завершает тело класса.
