namespace Simargl.FlawDetector.ViewModels; // Определяет пространство имён базовой инфраструктуры MVVM.

/// <summary> // Описывает назначение класса.
/// Предоставляет базовую реализацию уведомлений об изменении свойств. // Уточняет ответственность базового класса MVVM.
/// </summary> // Завершает XML-документацию класса.
internal abstract class ObservableObject : INotifyPropertyChanged // Объявляет базовый класс с уведомлениями.
{ // Начинает тело класса.
    /// <summary> // Документирует событие изменения свойств.
    /// Возникает после изменения значения свойства. // Уточняет семантику события.
    /// </summary> // Завершает XML-документацию события.
    public event PropertyChangedEventHandler? PropertyChanged; // Объявляет событие изменения свойств.

    /// <summary> // Документирует универсальный метод установки свойства.
    /// Обновляет хранилище свойства и публикует уведомление об изменении при необходимости. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <typeparam name="T">Тип значения свойства.</typeparam> // Документирует тип значения свойства.
    /// <param name="storage">Ссылка на хранилище свойства.</param> // Документирует хранилище свойства.
    /// <param name="value">Новое значение свойства.</param> // Документирует новое значение свойства.
    /// <param name="propertyName">Имя изменяемого свойства.</param> // Документирует имя свойства.
    /// <returns><see langword="true"/>, если значение изменилось; иначе <see langword="false"/>.</returns> // Документирует результат выполнения.
    protected bool SetProperty<T>(ref T storage, T value, [CallerMemberName] string? propertyName = null) // Объявляет метод установки свойства.
    { // Начинает тело метода установки свойства.
        if (EqualityComparer<T>.Default.Equals(storage, value)) // Проверяет фактическое изменение значения.
        { // Начинает ветку отсутствия изменений.
            return false; // Возвращает признак отсутствия обновления.
        } // Завершает ветку отсутствия изменений.

        storage = value; // Обновляет хранилище свойства.
        OnPropertyChanged(propertyName); // Публикует уведомление об изменении свойства.
        return true; // Возвращает признак успешного обновления.
    } // Завершает метод установки свойства.

    /// <summary> // Документирует метод публикации уведомления.
    /// Вызывает событие изменения свойства. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <param name="propertyName">Имя свойства.</param> // Документирует имя свойства.
    protected void OnPropertyChanged([CallerMemberName] string? propertyName = null) // Объявляет метод публикации уведомления.
    { // Начинает тело метода публикации уведомления.
        PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(propertyName)); // Вызывает событие с именем свойства.
    } // Завершает метод публикации уведомления.
} // Завершает тело базового класса.

