using Simargl.FlawDetector.Models; // Подключает модель определения дефекта.

namespace Simargl.FlawDetector.ViewModels; // Определяет пространство имён view model дефекта.

/// <summary> // Описывает назначение класса.
/// Представляет редактируемые параметры одного дефекта в пользовательском интерфейсе. // Уточняет роль типа в интерфейсе.
/// </summary> // Завершает XML-документацию класса.
internal sealed class FaultDefinitionViewModel : ObservableObject // Объявляет view model одного дефекта.
{ // Начинает тело класса.
    private bool isEnabled; // Хранит признак включения дефекта.
    private double severity; // Хранит тяжесть дефекта.
    private double impactFactor; // Хранит интенсивность ударного воздействия.

    /// <summary> // Документирует конструктор.
    /// Инициализирует новую view model дефекта. // Уточняет назначение конструктора.
    /// </summary> // Завершает XML-документацию конструктора.
    /// <param name="faultType">Тип дефекта.</param> // Документирует тип дефекта.
    /// <param name="displayName">Имя дефекта для интерфейса.</param> // Документирует отображаемое имя.
    /// <param name="defaultSeverity">Начальная тяжесть дефекта.</param> // Документирует стартовую тяжесть.
    /// <param name="defaultImpactFactor">Начальный коэффициент воздействия.</param> // Документирует стартовую интенсивность.
    public FaultDefinitionViewModel(BearingFaultType faultType, string displayName, double defaultSeverity, double defaultImpactFactor) // Объявляет конструктор view model.
    { // Начинает тело конструктора.
        FaultType = faultType; // Сохраняет тип дефекта.
        DisplayName = displayName; // Сохраняет отображаемое имя дефекта.
        this.severity = defaultSeverity; // Инициализирует тяжесть дефекта.
        this.impactFactor = defaultImpactFactor; // Инициализирует интенсивность воздействия.
    } // Завершает тело конструктора.

    /// <summary> // Документирует тип дефекта.
    /// Получает тип дефекта подшипника. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public BearingFaultType FaultType { get; } // Хранит тип дефекта.

    /// <summary> // Документирует отображаемое имя.
    /// Получает имя дефекта для показа в интерфейсе. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public string DisplayName { get; } // Хранит отображаемое имя.

    /// <summary> // Документирует признак включения.
    /// Получает или задаёт участие дефекта в генерации сигнала. // Уточняет назначение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public bool IsEnabled // Объявляет свойство включения дефекта.
    { // Начинает тело свойства включения.
        get => this.isEnabled; // Возвращает текущее состояние включения.
        set => SetProperty(ref this.isEnabled, value); // Обновляет состояние включения.
    } // Завершает тело свойства включения.

    /// <summary> // Документирует тяжесть дефекта.
    /// Получает или задаёт тяжесть дефекта в диапазоне от 0 до 1. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public double Severity // Объявляет свойство тяжести дефекта.
    { // Начинает тело свойства тяжести.
        get => this.severity; // Возвращает текущую тяжесть.
        set => SetProperty(ref this.severity, Math.Clamp(value, 0d, 1d)); // Ограничивает и обновляет тяжесть.
    } // Завершает тело свойства тяжести.

    /// <summary> // Документирует коэффициент воздействия.
    /// Получает или задаёт базовую интенсивность ударного воздействия дефекта. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    public double ImpactFactor // Объявляет свойство интенсивности воздействия.
    { // Начинает тело свойства интенсивности воздействия.
        get => this.impactFactor; // Возвращает текущую интенсивность.
        set => SetProperty(ref this.impactFactor, Math.Clamp(value, 0.05d, 4d)); // Ограничивает и обновляет интенсивность.
    } // Завершает тело свойства интенсивности воздействия.

    /// <summary> // Документирует экспорт модели.
    /// Создаёт неизменяемую модель дефекта для сервиса генерации. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <returns>Модель дефекта для расчёта сигнала.</returns> // Документирует возвращаемое значение.
    public FaultDefinition ToModel() // Объявляет метод преобразования во внутреннюю модель.
    { // Начинает тело метода преобразования.
        return new FaultDefinition(FaultType, DisplayName, IsEnabled, Severity, ImpactFactor); // Возвращает модель для генератора.
    } // Завершает метод преобразования.
} // Завершает тело view model дефекта.

