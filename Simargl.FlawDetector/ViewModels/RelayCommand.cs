namespace Simargl.FlawDetector.ViewModels; // Определяет пространство имён команд MVVM.

/// <summary> // Описывает назначение класса.
/// Представляет команду WPF, основанную на делегатах выполнения и проверки доступности. // Уточняет роль команды в MVVM.
/// </summary> // Завершает XML-документацию класса.
internal sealed class RelayCommand : ICommand // Объявляет реализацию команды.
{ // Начинает тело класса.
    private readonly Action execute; // Хранит действие выполнения команды.
    private readonly Func<bool>? canExecute; // Хранит проверку доступности команды.

    /// <summary> // Документирует конструктор.
    /// Инициализирует новый экземпляр команды. // Уточняет назначение конструктора.
    /// </summary> // Завершает XML-документацию конструктора.
    /// <param name="execute">Логика выполнения команды.</param> // Документирует обязательное действие выполнения.
    /// <param name="canExecute">Дополнительная логика проверки доступности команды.</param> // Документирует необязательную проверку доступности.
    /// <exception cref="ArgumentNullException">Возникает, если делегат выполнения отсутствует.</exception> // Документирует ошибку отсутствия делегата.
    public RelayCommand(Action execute, Func<bool>? canExecute = null) // Объявляет конструктор команды.
    { // Начинает тело конструктора.
        this.execute = execute ?? throw new ArgumentNullException(nameof(execute)); // Сохраняет действие выполнения или сообщает об ошибке.
        this.canExecute = canExecute; // Сохраняет делегат проверки доступности.
    } // Завершает тело конструктора.

    /// <summary> // Документирует событие изменения доступности команды.
    /// Возникает, когда необходимо пересчитать состояние доступности команды. // Уточняет назначение события.
    /// </summary> // Завершает XML-документацию события.
    public event EventHandler? CanExecuteChanged; // Объявляет событие изменения доступности.

    /// <summary> // Документирует метод проверки доступности.
    /// Определяет, может ли команда быть выполнена в текущем состоянии. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <param name="parameter">Неиспользуемый параметр команды.</param> // Документирует входной параметр.
    /// <returns><see langword="true"/>, если выполнение разрешено; иначе <see langword="false"/>.</returns> // Документирует возвращаемое значение.
    public bool CanExecute(object? parameter) // Объявляет метод проверки доступности.
    { // Начинает тело метода проверки доступности.
        return this.canExecute?.Invoke() ?? true; // Возвращает результат делегата или значение по умолчанию.
    } // Завершает метод проверки доступности.

    /// <summary> // Документирует метод выполнения.
    /// Выполняет связанную логику команды. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <param name="parameter">Неиспользуемый параметр команды.</param> // Документирует входной параметр.
    public void Execute(object? parameter) // Объявляет метод выполнения команды.
    { // Начинает тело метода выполнения.
        this.execute(); // Выполняет связанную логику команды.
    } // Завершает метод выполнения команды.

    /// <summary> // Документирует публикацию изменения доступности.
    /// Уведомляет привязки WPF о пересчёте доступности команды. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    public void NotifyCanExecuteChanged() // Объявляет метод публикации изменения доступности.
    { // Начинает тело метода публикации.
        CanExecuteChanged?.Invoke(this, EventArgs.Empty); // Вызывает событие пересчёта доступности.
    } // Завершает метод публикации.
} // Завершает тело класса команды.

