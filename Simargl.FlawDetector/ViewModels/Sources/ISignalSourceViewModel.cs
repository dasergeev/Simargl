using Simargl.FlawDetector.Models; // Подключает перечисление типов источников сигнала.
using Simargl.FlawDetector.Models.Sources; // Подключает общий интерфейс моделей источников.

namespace Simargl.FlawDetector.ViewModels.Sources; // Определяет пространство имён view model источников сигнала.

/// <summary> // Описывает назначение интерфейса.
/// Определяет общий абстрактный контракт view model любого источника сигнала. // Уточняет роль интерфейса в интерфейсе приложения.
/// </summary> // Завершает XML-документацию интерфейса.
internal interface ISignalSourceViewModel // Объявляет общий интерфейс view model источника.
{ // Начинает тело интерфейса.
    /// <summary> // Документирует тип источника.
    /// Получает тип источника сигнала. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    SignalSourceType SourceType { get; } // Возвращает тип источника сигнала.

    /// <summary> // Документирует отображаемое имя.
    /// Получает имя источника для списка выбора. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    string DisplayName { get; } // Возвращает отображаемое имя источника.

    /// <summary> // Документирует признак включения.
    /// Получает или задаёт участие источника в генерации сигнала. // Уточняет значение свойства.
    /// </summary> // Завершает XML-документацию свойства.
    bool IsEnabled { get; set; } // Получает или задаёт признак включения источника.

    /// <summary> // Документирует экспорт модели.
    /// Создаёт модель источника сигнала для сервиса генерации. // Уточняет назначение метода.
    /// </summary> // Завершает XML-документацию метода.
    /// <returns>Модель источника сигнала.</returns> // Документирует возвращаемое значение.
    ISignalSource ToModel(); // Создаёт модель источника сигнала.
}
