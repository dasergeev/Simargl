using System.Runtime.CompilerServices;
using System.Text;

namespace Apeiron.Platform.Utilities.Generation;

/// <summary>
/// Представляет средство записи файла с исходным кодом.
/// </summary>
public sealed class SourceFileWriter :
    IDisposable
{
    /// <summary>
    /// Поле для хранения средства записи в файл.
    /// </summary>
    private readonly StreamWriter _StreamWriter;

    /// <summary>
    /// Поле для хранения флага начала строки.
    /// </summary>
    private bool _IsStartOfLine = false;

    /// <summary>
    /// Поле для хранения начала строки текста.
    /// </summary>
    private string _StartOfLineText = string.Empty;

    /// <summary>
    /// Поле для хранения стека начальных строк текста.
    /// </summary>
    private readonly Stack<string> _StartOfLines;

    /// <summary>
    /// Инициализирует новый экземпляр класса.
    /// </summary>
    /// <param name="path">
    /// Путь к файлу.
    /// </param>
    public SourceFileWriter(string path)
    {
        //  Получение информации о файле.
        FileInfo fileInfo = new(path);

        //  Создание каталога.
        fileInfo.Directory?.Create();

        //  Создание средства записи в файл.
        _StreamWriter = new(path, false, Encoding.UTF8);

        //  Создание стека начальных строк текста.
        _StartOfLines = new();

        //  Запись информации о генерации кода.
        WriteLine(
$@"//------------------------------------------------------------------------------
// <auto-generated>
//     Этот код создан программой ""Platform\Utilities\Generation"".
//     Время создания: {DateTime.Now:yyyy.MM.dd HH:mm:ss}
//
//     Изменения в этом файле могут привести к неправильной работе
//     и будут потеряны в случае повторной генерации кода.
// </auto-generated>
//------------------------------------------------------------------------------

//  Необходимо указывать явно при автоматической генерации кода.
#nullable enable");

        //  Переход на новую строку.
        WriteLine();
    }

    /// <summary>
    /// Выполняет запись начала блока.
    /// </summary>
    public void BeginBlock()
    {
        //  Запись символа начала блока.
        WriteLine("{");

        //  Добавление отступа.
        IndentPush();
    }

    /// <summary>
    /// Выполняет запись конца блока.
    /// </summary>
    public void EndBlock(string extra = "")
    {
        //  Уменьшение отступа текста.
        IndentPop();

        //  Запись символа конца блока.
        WriteLine("}" + extra);
    }

    /// <summary>
    /// Добавляет отступ текста.
    /// </summary>
    /// <param name="text">
    /// Добавочный текст начала строки.
    /// </param>
    public void IndentPush(string text = "    ")
    {
        //  Помещение текущего значения начала строки текста.
        _StartOfLines.Push(_StartOfLineText);

        //  Корректировка начала строки текста.
        _StartOfLineText += text;
    }

    /// <summary>
    /// Уменьшает отступ текста.
    /// </summary>
    public void IndentPop()
    {
        //  Получение предыдущего значения начала строки текста.
        _StartOfLineText = _StartOfLines.Pop();
    }

    /// <summary>
    /// Выполняет переход на новую строку.
    /// </summary>
    public void WriteLine()
    {
        //  Запись пустой строки.
        WriteLine(string.Empty);
    }

    /// <summary>
    /// Выполняет запись текста и переход на новую строку.
    /// </summary>
    /// <param name="text">
    /// Текст, который необходимо записать.
    /// </param>
    public void WriteLine(string text)
    {
        //  Запись текста.
        Write(text);

        //  Переход на новую строку.
        Write("\n");
    }

    /// <summary>
    /// Выполняет запись текста.
    /// </summary>
    /// <param name="text">
    /// Текст, который необходимо записать.
    /// </param>
    public void Write(string text)
    {
        //  Проверка строки.
        if (string.IsNullOrEmpty(text))
        {
            //  Нечего записывать.
            return;
        }

        //  Преобразование текста перед разбивкой.
        text = text.Replace("\r\n", "\n").Replace('\r', '\n');

        //  Разбивка текста.
        string[] parts = text.Split('\n');

        //  Проверка флага начала строки.
        if (_IsStartOfLine)
        {
            //  Запись начала строки текста.
            NewLine();
        }

        //  Запись первой части.
        WriteCore(parts[0]);

        //  Проверка количества частей.
        if (parts.Length != 1)
        {
            //  Запись промежуточных частей.
            for (int i = 1; i < parts.Length - 1; i++)
            {
                //  Запись начала строки текста.
                NewLine();

                //  Запись части.
                WriteCore(parts[i]);
            }

            //  Проверка последней части.
            if (parts[^1].Length != 0)
            {
                //  Запись начала строки текста.
                NewLine();

                //  Запись части.
                WriteCore(parts[^1]);
            }
        }

        //  Установка флана начала строки.
        _IsStartOfLine = parts[^1].Length == 0;
    }

    /// <summary>
    /// Выполняет переход на новую строку.
    /// </summary>
    private void NewLine()
    {
        //  Запись символов перехода на новую строку.
        WriteCore("\r\n");

        //  Запись начала строки текста.
        WriteCore(_StartOfLineText);
    }

    /// <summary>
    /// Выполняет запись текста в файл.
    /// </summary>
    /// <param name="text">
    /// Текст, который необходимо записать в файл.
    /// </param>
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private void WriteCore(string text)
    {
        _StreamWriter.Write(text);
    }

    /// <summary>
    /// Разрушает объект.
    /// </summary>
    void IDisposable.Dispose()
    {
        //  Разрушение средства записи в файл.
        ((IDisposable)_StreamWriter).Dispose();
    }
}
