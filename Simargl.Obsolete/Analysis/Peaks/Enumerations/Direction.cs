namespace Simargl.Analysis.Peaks;

/// <summary>
/// Представляет значение, определяющее направление движения.
/// </summary>
public enum Direction
{
    /// <summary>
    /// Прямое направление движения.
    /// </summary>
    Direct = 0,

    /// <summary>
    /// Обратное направление движения.
    /// </summary>
    Reverse = 1,

    /// <summary>
    /// Неизвестное направление.
    /// </summary>
    Other = 2
}
